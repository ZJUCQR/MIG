def normalize_text(text: str) -> str:
    return " ".join(text.split())


def expected_image_count(item: Dict[str, Any]) -> int:
    num_images = item.get("num_images", 0)
    try:
        value = int(num_images)
    except Exception:
        value = len(item.get("sub_prompts", []))
    return max(1, value)


def load_prompt_specs(prompt_file: Path) -> List[PromptSpec]:
    data = json.loads(prompt_file.read_text(encoding="utf-8"))
    if not isinstance(data, list):
        raise ValueError(f"Expected JSON array in {prompt_file}")

    specs: List[PromptSpec] = []
    for item in data:
        prompt_id = normalize_text(str(item.get("id", "")))
        if not prompt_id:
            raise ValueError("Every prompt item must contain a non-empty 'id'.")
        prompt = normalize_text(str(item.get("prompt", "")))
        global_prompt = normalize_text(str(item.get("global_prompt", "")))
        sub_prompts = tuple(
            normalized
            for normalized in (
                normalize_text(str(entry))
                for entry in item.get("sub_prompts", [])
            )
            if normalized
        )
        specs.append(
            PromptSpec(
                prompt_id=prompt_id,
                prompt=prompt,
                global_prompt=global_prompt,
                sub_prompts=sub_prompts,
                expected_images=expected_image_count(item),
            )
        )
    return specs


def parse_requested_metrics(raw_metrics: str) -> Tuple[str, ...]:
    if not raw_metrics.strip():
        raise ValueError("At least one metric must be requested.")

    expanded: List[str] = []
    for item in raw_metrics.split(","):
        metric = item.strip()
        if not metric:
            continue
        if metric in METRIC_ALIASES:
            expanded.extend(METRIC_ALIASES[metric])
            continue
        if metric not in ALL_METRICS:
            raise ValueError(f"Unsupported metric: {metric}")
        expanded.append(metric)

    deduped: List[str] = []
    seen = set()
    for metric in expanded:
        if metric not in seen:
            deduped.append(metric)
            seen.add(metric)

    if not deduped:
        raise ValueError("No valid metrics were requested.")
    return tuple(deduped)


def image_sort_key(path: Path) -> Tuple[int, List[int], str]:
    numbers = [int(token) for token in re.findall(r"\d+", path.stem)]
    if numbers:
        return (0, numbers, path.stem.lower())
    return (1, [10**9], path.stem.lower())


def list_group_images(group_dir: Path) -> List[Path]:
    if not group_dir.exists() or not group_dir.is_dir():
        return []
    image_paths = [
        path
        for path in group_dir.iterdir()
        if path.is_file() and path.suffix.lower() in IMAGE_EXTENSIONS
    ]
    return sorted(image_paths, key=image_sort_key)


def mean_or_none(values: Sequence[Optional[float]]) -> Optional[float]:
    filtered = [float(value) for value in values if value is not None]
    if not filtered:
        return None
    return float(sum(filtered) / len(filtered))


def prompt_text_for_image(spec: PromptSpec, image_index: int) -> str:
    if image_index < len(spec.sub_prompts):
        return spec.sub_prompts[image_index]
    if spec.global_prompt:
        return spec.global_prompt
    if spec.prompt:
        return spec.prompt
    return ""


def global_prompt_for_group(spec: PromptSpec) -> str:
    if spec.global_prompt:
        return spec.global_prompt
    return spec.prompt


def chunked(items: Sequence[Any], chunk_size: int) -> Iterable[Sequence[Any]]:
    for start in range(0, len(items), chunk_size):
        yield items[start : start + chunk_size]


def cosine_similarity(embeddings_a: torch.Tensor, embeddings_b: torch.Tensor) -> torch.Tensor:
    return (embeddings_a * embeddings_b).sum(dim=-1)


def load_rgb_image(path: Path) -> Image.Image:
    with Image.open(path) as image:
        return image.convert("RGB")


def ensure_metrics_supported(
    requested_metrics: Sequence[str],
    face_parsing_path: Path,
) -> None:
    if "fgis_anchor" in requested_metrics and not face_parsing_path.exists():
        raise FileNotFoundError(
            f"FGIS requires face_parsing.pth, but it was not found at {face_parsing_path}."
        )


def to_absolute_string(path: Path) -> str:
    return str(path.resolve())


def serialize_csv_value(value: Any) -> Any:
    if value is None:
        return ""
    if isinstance(value, Path):
        return to_absolute_string(value)
    return value


def group_status(expected_images: int, found_images: int) -> str:
    if found_images == 0:
        return "missing"
    if found_images < expected_images:
        return "partial"
    return "ok"


def expand_box(
    box: Tuple[float, float, float, float],
    image_size: Tuple[int, int],
    scale: float = 1.15,
) -> Tuple[int, int, int, int]:
    width, height = image_size
    x1, y1, x2, y2 = box
    box_width = max(x2 - x1, 1.0)
    box_height = max(y2 - y1, 1.0)
    center_x = x1 + box_width / 2.0
    center_y = y1 + box_height / 2.0
    new_width = box_width * scale
    new_height = box_height * scale
    new_x1 = max(int(round(center_x - new_width / 2.0)), 0)
    new_y1 = max(int(round(center_y - new_height / 2.0)), 0)
    new_x2 = min(int(round(center_x + new_width / 2.0)), width)
    new_y2 = min(int(round(center_y + new_height / 2.0)), height)
    if new_x2 <= new_x1:
        new_x2 = min(new_x1 + 1, width)
    if new_y2 <= new_y1:
        new_y2 = min(new_y1 + 1, height)
    return (new_x1, new_y1, new_x2, new_y2)


class ClipScorer:
    def __init__(self, model_name: str, device: str, batch_size: int) -> None:
        from transformers import CLIPModel, CLIPProcessor

        self.device = torch.device(device)
        self.batch_size = batch_size
        self.model = CLIPModel.from_pretrained(model_name).to(self.device)
        self.model.eval()
        self.processor = CLIPProcessor.from_pretrained(model_name)
        self.text_cache: Dict[str, torch.Tensor] = {}

    def encode_texts(self, texts: Sequence[str]) -> torch.Tensor:
        missing = list(dict.fromkeys(text for text in texts if text not in self.text_cache))
        for batch in chunked(missing, self.batch_size):
            inputs = self.processor(
                text=list(batch),
                return_tensors="pt",
                padding=True,
                truncation=True,
            )
            inputs = {key: value.to(self.device) for key, value in inputs.items()}
            with torch.no_grad():
                features = self.model.get_text_features(**inputs)
            features = F.normalize(features, dim=-1).cpu()
            for text, feature in zip(batch, features):
                self.text_cache[text] = feature
        return torch.stack([self.text_cache[text] for text in texts], dim=0)

    def encode_images(self, images: Sequence[Image.Image]) -> torch.Tensor:
        features: List[torch.Tensor] = []
        for batch in chunked(images, self.batch_size):
            inputs = self.processor(images=list(batch), return_tensors="pt")
            pixel_values = inputs["pixel_values"].to(self.device)
            with torch.no_grad():
                batch_features = self.model.get_image_features(pixel_values=pixel_values)
            features.append(F.normalize(batch_features, dim=-1).cpu())
        return torch.cat(features, dim=0)


class DinoScorer:
    def __init__(
        self,
        model_name: str,
        device: str,
        batch_size: int,
        checkpoint_path: Optional[str] = None,
        cache_dir: Optional[str] = None,
    ) -> None:
        import timm
        from torchvision import transforms
        from torchvision.transforms import InterpolationMode

        self.device = torch.device(device)
        self.batch_size = batch_size
        resolved_model_name = self._resolve_model_name(model_name)
        resolved_checkpoint_path = checkpoint_path
        if resolved_checkpoint_path is None:
            resolved_checkpoint_path = self._maybe_auto_download_checkpoint(
                resolved_model_name,
                cache_dir,
            )

        timm_kwargs: Dict[str, Any] = {
            "model_name": resolved_model_name,
            "num_classes": 0,
        }
        try:
            if resolved_checkpoint_path:
                timm_kwargs["pretrained"] = False
                self.model = timm.create_model(**timm_kwargs).to(self.device)
                state_dict = self._load_checkpoint_state_dict(Path(resolved_checkpoint_path))
                incompatible = self.model.load_state_dict(state_dict, strict=False)
                missing = sorted(set(incompatible.missing_keys))
                unexpected = sorted(set(incompatible.unexpected_keys))
                if missing:
                    print(
                        f"[DINO] Loaded checkpoint with missing keys: {missing[:10]}"
                        + (" ..." if len(missing) > 10 else "")
                    )
                if unexpected:
                    print(
                        f"[DINO] Ignored unexpected keys: {unexpected[:10]}"
                        + (" ..." if len(unexpected) > 10 else "")
                    )
            else:
                timm_kwargs["pretrained"] = True
                self.model = timm.create_model(model_name, pretrained=True, num_classes=0).to(self.device)
        except RuntimeError as error:
            message = str(error)
            if "Unknown model" in message:
                if resolved_checkpoint_path:
                    raise RuntimeError(
                        f"Unknown model ({resolved_model_name}). When you pass --dino-checkpoint, "
                        "use a timm architecture name such as 'vit_small_patch16_224' "
                        "or 'vit_base_patch16_224'."
                    ) from error
                raise RuntimeError(
                    f"Unknown model ({model_name}). Your timm version does not expose this DINO pretrained tag. "
                    "Fix options: "
                    "1) pass --dino-model vit_small_patch16_224 --dino-checkpoint /path/to/dino_vits16.pth; "
                    "2) upgrade timm to a version that supports this tag; "
                    "3) temporarily remove DINO-based metrics: dino_anchor and fgis_anchor."
                ) from error
            raise
        self.model.eval()
        self.transform = transforms.Compose(
            [
                transforms.Resize(256, interpolation=InterpolationMode.BICUBIC),
                transforms.CenterCrop(224),
                transforms.ToTensor(),
                transforms.Normalize(
                    mean=(0.485, 0.456, 0.406),
                    std=(0.229, 0.224, 0.225),
                ),
            ]
        )

    def encode_images(self, images: Sequence[Image.Image]) -> torch.Tensor:
        features: List[torch.Tensor] = []
        for batch in chunked(images, self.batch_size):
            tensors = torch.stack([self.transform(image) for image in batch], dim=0)
            tensors = tensors.to(self.device)
            with torch.no_grad():
                batch_features = self.model(tensors)
            if not isinstance(batch_features, torch.Tensor):
                raise TypeError("Unexpected DINO model output type.")
            features.append(F.normalize(batch_features, dim=-1).cpu())
        return torch.cat(features, dim=0)

    @staticmethod
    def _resolve_model_name(model_name: str) -> str:
        return DINO_MODEL_ALIASES.get(model_name, model_name)

    @classmethod
    def _maybe_auto_download_checkpoint(
        cls,
        model_name: str,
        cache_dir: Optional[str],
    ) -> Optional[str]:
        spec = DINO_AUTO_CHECKPOINTS.get(model_name)
        if spec is None:
            return None

        from torch.hub import download_url_to_file

        target_dir = Path(cache_dir) if cache_dir else Path(default_dino_cache_dir())
        target_dir.mkdir(parents=True, exist_ok=True)
        checkpoint_path = target_dir / spec["filename"]
        if not checkpoint_path.exists():
            print(f"[DINO] Downloading official checkpoint to {checkpoint_path}")
            download_url_to_file(spec["url"], str(checkpoint_path), progress=True)
        else:
            print(f"[DINO] Reusing cached checkpoint: {checkpoint_path}")
        return str(checkpoint_path)

    @staticmethod
    def _strip_known_prefixes(key: str) -> str:
        prefixes = ("module.", "backbone.", "model.", "teacher.", "student.")
        changed = True
        while changed:
            changed = False
            for prefix in prefixes:
                if key.startswith(prefix):
                    key = key[len(prefix) :]
                    changed = True
        return key

    @classmethod
    def _load_checkpoint_state_dict(cls, checkpoint_path: Path) -> Dict[str, torch.Tensor]:
        checkpoint = torch.load(checkpoint_path, map_location="cpu")
        if isinstance(checkpoint, dict):
            for key in ("teacher", "student", "state_dict", "model"):
                candidate = checkpoint.get(key)
                if isinstance(candidate, dict):
                    checkpoint = candidate
                    break
        if not isinstance(checkpoint, dict):
            raise TypeError(f"Unsupported checkpoint format: {checkpoint_path}")

        cleaned: Dict[str, torch.Tensor] = {}
        for key, value in checkpoint.items():
            if not isinstance(value, torch.Tensor):
                continue
            normalized_key = cls._strip_known_prefixes(key)
            cleaned[normalized_key] = value
        if not cleaned:
            raise ValueError(f"No tensor weights were found in checkpoint: {checkpoint_path}")
        return cleaned


class FaceNetAnalyzer:
    def __init__(self, device: str) -> None:
        try:
            from facenet_pytorch import InceptionResnetV1, MTCNN
        except ImportError as error:
            raise ImportError(
                "FaceSim requires facenet-pytorch. Install dependencies from requirements.txt."
            ) from error

        self.device = torch.device(device)
        self.detector = MTCNN(
            image_size=160,
            margin=0,
            keep_all=True,
            select_largest=True,
            device=self.device,
        )
        self.aligner = MTCNN(
            image_size=160,
            margin=0,
            keep_all=False,
            select_largest=True,
            device=self.device,
        )
        self.model = InceptionResnetV1(pretrained="vggface2").eval().to(self.device)

    def _select_box(
        self,
        boxes: np.ndarray,
        probabilities: Optional[np.ndarray],
    ) -> Tuple[float, float, float, float]:
        best_index = 0
        best_area = -1.0
        best_probability = -1.0
        for index, box in enumerate(boxes):
            x1, y1, x2, y2 = box.tolist()
            area = max(x2 - x1, 0.0) * max(y2 - y1, 0.0)
            probability = float(probabilities[index]) if probabilities is not None else 0.0
            if area > best_area or (area == best_area and probability > best_probability):
                best_index = index
                best_area = area
                best_probability = probability
        selected = boxes[best_index]
        return tuple(float(value) for value in selected.tolist())

    def analyze_image(self, image: Image.Image) -> FaceAnalysisResult:
        boxes, probabilities = self.detector.detect(image)
        box: Optional[Tuple[float, float, float, float]] = None
        if boxes is not None and len(boxes) > 0:
            box = self._select_box(boxes, probabilities)

        aligned = self.aligner(image)
        if aligned is None:
            return FaceAnalysisResult(embedding=None, box=box)
        if aligned.ndim == 3:
            aligned = aligned.unsqueeze(0)
        aligned = aligned.to(self.device)
        with torch.no_grad():
            embedding = self.model(aligned)
        embedding = F.normalize(embedding, dim=-1).cpu().squeeze(0)
        return FaceAnalysisResult(embedding=embedding, box=box)


class FaceParser:
    def __init__(self, checkpoint_path: Path, device: str) -> None:
        from torchvision import transforms

        from models.BiSeNet.model import BiSeNet

        self.device = torch.device(device)
        self.model = BiSeNet(n_classes=19)
        state_dict = torch.load(checkpoint_path, map_location=self.device)
        self.model.load_state_dict(state_dict)
        self.model.to(self.device)
        self.model.eval()
        self.transform = transforms.Compose(
            [
                transforms.ToTensor(),
                transforms.Normalize(
                    (0.485, 0.456, 0.406),
                    (0.229, 0.224, 0.225),
                ),
            ]
        )

    def extract_facial_region(
        self,
        image: Image.Image,
        box: Tuple[float, float, float, float],
    ) -> Optional[Image.Image]:
        crop_box = expand_box(box, image.size, scale=1.15)
        cropped = image.crop(crop_box)
        if cropped.size[0] < 2 or cropped.size[1] < 2:
            return None

        resized = cropped.resize((512, 512), Image.BILINEAR)
        tensor = self.transform(resized).unsqueeze(0).to(self.device)
        with torch.no_grad():
            output = self.model(tensor)[0]
        parsing = output.squeeze(0).argmax(0).cpu().numpy()
        mask = np.isin(parsing, FACIAL_PART_IDS)
        if not mask.any():
            return None

        resized_mask = Image.fromarray((mask.astype(np.uint8) * 255)).resize(
            cropped.size,
            Image.NEAREST,
        )
        mask_array = np.asarray(resized_mask) > 0
        if not mask_array.any():
            return None

        cropped_array = np.asarray(cropped)
        masked_array = np.zeros_like(cropped_array)
        masked_array[mask_array] = cropped_array[mask_array]
        ys, xs = np.where(mask_array)
        face_bbox = (
            int(xs.min()),
            int(ys.min()),
            int(xs.max()) + 1,
            int(ys.max()) + 1,
        )
        region = Image.fromarray(masked_array).crop(face_bbox)
        if region.size[0] < 2 or region.size[1] < 2:
            return None
        return region


def build_group_row(
    spec: PromptSpec,
    group_dir: Path,
    image_paths: Sequence[Path],
    anchor_index: int,
) -> Dict[str, Any]:
    found_images = len(image_paths)
    return {
        "prompt_id": spec.prompt_id,
        "group_dir": to_absolute_string(group_dir),
        "status": group_status(spec.expected_images, found_images),
        "expected_images": spec.expected_images,
        "found_images": found_images,
        "anchor_index": anchor_index,
        "clip_t_sub_mean": None,
        "clip_t_global_mean": None,
        "clip_i_anchor_mean": None,
        "dino_anchor_mean": None,
        "facesim_anchor_mean": None,
        "fgis_anchor_mean": None,
        "facesim_valid_ratio": None,
        "fgis_valid_ratio": None,
    }


def build_image_rows(
    spec: PromptSpec,
    image_paths: Sequence[Path],
    anchor_position: int,
) -> List[Dict[str, Any]]:
    rows: List[Dict[str, Any]] = []
    group_prompt = global_prompt_for_group(spec)
    for index, image_path in enumerate(image_paths):
        rows.append(
            {
                "prompt_id": spec.prompt_id,
                "expected_images": spec.expected_images,
                "found_images": len(image_paths),
                "image_index": index + 1,
                "image_name": image_path.name,
                "image_path": to_absolute_string(image_path),
                "is_anchor": index == anchor_position,
                "sub_prompt_used": prompt_text_for_image(spec, index),
                "global_prompt": group_prompt,
                "clip_t_sub": None,
                "clip_t_global": None,
                "clip_i_anchor": None,
                "dino_anchor": None,
                "facesim_anchor": None,
                "fgis_anchor": None,
                "face_detected": None,
                "facial_region_ready": None,
            }
        )
    return rows


def evaluate_group(
    spec: PromptSpec,
    image_root: Path,
    requested_metrics: Sequence[str],
    anchor_position: int,
    clip_scorer: Optional[ClipScorer],
    dino_scorer: Optional[DinoScorer],
    face_analyzer: Optional[FaceNetAnalyzer],
    face_parser: Optional[FaceParser],
) -> Tuple[Dict[str, Any], List[Dict[str, Any]]]:
    group_dir = image_root / spec.prompt_id
    image_paths = list_group_images(group_dir)
    group_row = build_group_row(spec, group_dir, image_paths, anchor_position + 1)
    image_rows = build_image_rows(spec, image_paths, anchor_position)

    if not image_paths:
        return group_row, image_rows

    images = [load_rgb_image(path) for path in image_paths]

    clip_image_embeddings: Optional[torch.Tensor] = None
    if clip_scorer is not None:
        clip_image_embeddings = clip_scorer.encode_images(images)

    if "clip_t_sub" in requested_metrics and clip_scorer is not None and clip_image_embeddings is not None:
        sub_prompts = [row["sub_prompt_used"] for row in image_rows]
        prompt_embeddings = clip_scorer.encode_texts(sub_prompts)
        scores = cosine_similarity(clip_image_embeddings, prompt_embeddings).tolist()
        for row, score in zip(image_rows, scores):
            row["clip_t_sub"] = float(score)
        group_row["clip_t_sub_mean"] = mean_or_none([row["clip_t_sub"] for row in image_rows])

    if "clip_t_global" in requested_metrics and clip_scorer is not None and clip_image_embeddings is not None:
        group_prompt = global_prompt_for_group(spec)
        global_prompts = [group_prompt for _ in image_rows]
        prompt_embeddings = clip_scorer.encode_texts(global_prompts)
        scores = cosine_similarity(clip_image_embeddings, prompt_embeddings).tolist()
        for row, score in zip(image_rows, scores):
            row["clip_t_global"] = float(score)
        group_row["clip_t_global_mean"] = mean_or_none([row["clip_t_global"] for row in image_rows])

    max_anchor_comparisons = max(len(image_rows) - 1, 0)
    if anchor_position >= len(image_rows):
        return group_row, image_rows

    if (
        "clip_i_anchor" in requested_metrics
        and clip_scorer is not None
        and clip_image_embeddings is not None
        and len(image_rows) > 1
    ):
        anchor_embedding = clip_image_embeddings[anchor_position].unsqueeze(0)
        anchor_scores = cosine_similarity(
            clip_image_embeddings,
            anchor_embedding.expand_as(clip_image_embeddings),
        ).tolist()
        for index, row in enumerate(image_rows):
            if index == anchor_position:
                continue
            row["clip_i_anchor"] = float(anchor_scores[index])
        group_row["clip_i_anchor_mean"] = mean_or_none([row["clip_i_anchor"] for row in image_rows])

    dino_image_embeddings: Optional[torch.Tensor] = None
    if ("dino_anchor" in requested_metrics or "fgis_anchor" in requested_metrics) and dino_scorer is not None:
        dino_image_embeddings = dino_scorer.encode_images(images)

    if (
        "dino_anchor" in requested_metrics
        and dino_scorer is not None
        and dino_image_embeddings is not None
        and len(image_rows) > 1
    ):
        anchor_embedding = dino_image_embeddings[anchor_position].unsqueeze(0)
        anchor_scores = cosine_similarity(
            dino_image_embeddings,
            anchor_embedding.expand_as(dino_image_embeddings),
        ).tolist()
        for index, row in enumerate(image_rows):
            if index == anchor_position:
                continue
            row["dino_anchor"] = float(anchor_scores[index])
        group_row["dino_anchor_mean"] = mean_or_none([row["dino_anchor"] for row in image_rows])

    face_results: Optional[List[FaceAnalysisResult]] = None
    if ("facesim_anchor" in requested_metrics or "fgis_anchor" in requested_metrics) and face_analyzer is not None:
        face_results = [face_analyzer.analyze_image(image) for image in images]
        for row, result in zip(image_rows, face_results):
            row["face_detected"] = result.box is not None

    if "facesim_anchor" in requested_metrics and face_results is not None and len(image_rows) > 1:
        anchor_embedding = face_results[anchor_position].embedding
        valid_faces = 0
        if anchor_embedding is not None:
            for index, row in enumerate(image_rows):
                if index == anchor_position:
                    continue
                target_embedding = face_results[index].embedding
                if target_embedding is None:
                    continue
                score = float(
                    cosine_similarity(anchor_embedding.unsqueeze(0), target_embedding.unsqueeze(0))[0].item()
                )
                row["facesim_anchor"] = score
                valid_faces += 1
        group_row["facesim_anchor_mean"] = mean_or_none([row["facesim_anchor"] for row in image_rows])
        if max_anchor_comparisons > 0:
            group_row["facesim_valid_ratio"] = float(valid_faces / max_anchor_comparisons)

    if (
        "fgis_anchor" in requested_metrics
        and face_results is not None
        and face_parser is not None
        and dino_scorer is not None
        and len(image_rows) > 1
    ):
        facial_region_images: Dict[int, Image.Image] = {}
        for index, result in enumerate(face_results):
            if result.box is None:
                image_rows[index]["facial_region_ready"] = False
                continue
            region = face_parser.extract_facial_region(images[index], result.box)
            image_rows[index]["facial_region_ready"] = region is not None
            if region is not None:
                facial_region_images[index] = region

        valid_regions = 0
        if anchor_position in facial_region_images:
            region_indices = sorted(facial_region_images.keys())
            region_embeddings = dino_scorer.encode_images([facial_region_images[index] for index in region_indices])
            region_embedding_map = {
                index: embedding
                for index, embedding in zip(region_indices, region_embeddings)
            }
            anchor_embedding = region_embedding_map[anchor_position]
            for index, row in enumerate(image_rows):
                if index == anchor_position:
                    continue
                target_embedding = region_embedding_map.get(index)
                if target_embedding is None:
                    continue
                score = float(
                    cosine_similarity(anchor_embedding.unsqueeze(0), target_embedding.unsqueeze(0))[0].item()
                )
                row["fgis_anchor"] = score
                valid_regions += 1
        group_row["fgis_anchor_mean"] = mean_or_none([row["fgis_anchor"] for row in image_rows])
        if max_anchor_comparisons > 0:
            group_row["fgis_valid_ratio"] = float(valid_regions / max_anchor_comparisons)

    return group_row, image_rows


def write_csv(output_path: Path, rows: Sequence[Dict[str, Any]], fieldnames: Sequence[str]) -> None:
    with output_path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        for row in rows:
            writer.writerow({field: serialize_csv_value(row.get(field)) for field in fieldnames})


def build_summary(
    args: argparse.Namespace,
    requested_metrics: Sequence[str],
    prompt_specs: Sequence[PromptSpec],
    group_rows: Sequence[Dict[str, Any]],
    image_rows: Sequence[Dict[str, Any]],
) -> Dict[str, Any]:
    summary: Dict[str, Any] = {
        "config": {
            "prompt_file": to_absolute_string(Path(args.prompt_file)),
            "image_root": to_absolute_string(Path(args.image_root)),
            "output_dir": to_absolute_string(Path(args.output_dir)),
            "metrics": list(requested_metrics),
            "anchor_index": args.anchor_index,
            "device": args.device,
            "clip_model": args.clip_model,
            "dino_model": args.dino_model,
            "dino_checkpoint": args.dino_checkpoint,
            "dino_cache_dir": to_absolute_string(Path(args.dino_cache_dir)),
            "face_parsing_path": to_absolute_string(Path(args.face_parsing_path)),
            "face_detector_device": args.face_detector_device or args.device,
            "batch_size": args.batch_size,
        },
        "counts": {
            "prompt_items": len(prompt_specs),
            "groups_with_images": sum(1 for row in group_rows if row["found_images"] > 0),
            "groups_missing_images": sum(1 for row in group_rows if row["status"] == "missing"),
            "groups_partial": sum(1 for row in group_rows if row["status"] == "partial"),
            "total_images_found": len(image_rows),
        },
        "metrics": {},
        "coverage": {},
    }

    for metric in ALL_METRICS:
        values = [row.get(metric) for row in image_rows]
        filtered = [float(value) for value in values if value is not None]
        summary["metrics"][metric] = {
            "mean": mean_or_none(filtered),
            "count": len(filtered),
        }

    for key in ("facesim_valid_ratio", "fgis_valid_ratio"):
        values = [row.get(key) for row in group_rows if row.get(key) is not None]
        summary["coverage"][key] = {
            "mean": mean_or_none(values),
            "count": len(values),
        }

    return summary


def main() -> None:
    args = parse_args()
    requested_metrics = parse_requested_metrics(args.metrics)
    prompt_file = Path(args.prompt_file)
    image_root = Path(args.image_root)
    output_dir = Path(args.output_dir)
    face_parsing_path = Path(args.face_parsing_path)

    if not prompt_file.exists():
        raise SystemExit(f"Prompt JSON not found: {prompt_file}")
    if not image_root.exists():
        raise SystemExit(f"Image root not found: {image_root}")

    ensure_metrics_supported(requested_metrics, face_parsing_path)
    prompt_specs = load_prompt_specs(prompt_file)

    needs_clip = any(metric.startswith("clip_") for metric in requested_metrics)
    needs_dino = "dino_anchor" in requested_metrics or "fgis_anchor" in requested_metrics
    needs_faces = "facesim_anchor" in requested_metrics or "fgis_anchor" in requested_metrics

    clip_scorer = ClipScorer(args.clip_model, args.device, args.batch_size) if needs_clip else None
    dino_scorer = DinoScorer(
        args.dino_model,
        args.device,
        args.batch_size,
        checkpoint_path=args.dino_checkpoint,
        cache_dir=args.dino_cache_dir,
    ) if needs_dino else None
    face_analyzer = FaceNetAnalyzer(args.face_detector_device or args.device) if needs_faces else None
    face_parser = FaceParser(face_parsing_path, args.device) if "fgis_anchor" in requested_metrics else None

    anchor_position = args.anchor_index - 1
    per_group_rows: List[Dict[str, Any]] = []
    per_image_rows: List[Dict[str, Any]] = []

    for spec in prompt_specs:
        group_row, image_rows = evaluate_group(
            spec=spec,
            image_root=image_root,
            requested_metrics=requested_metrics,
            anchor_position=anchor_position,
            clip_scorer=clip_scorer,
            dino_scorer=dino_scorer,
            face_analyzer=face_analyzer,
            face_parser=face_parser,
        )
        per_group_rows.append(group_row)
        per_image_rows.extend(image_rows)

    output_dir.mkdir(parents=True, exist_ok=True)
    summary = build_summary(args, requested_metrics, prompt_specs, per_group_rows, per_image_rows)
    (output_dir / "summary.json").write_text(
        json.dumps(summary, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )

    write_csv(
        output_dir / "per_group.csv",
        per_group_rows,
        [
            "prompt_id",
            "group_dir",
            "status",
            "expected_images",
            "found_images",
            "anchor_index",
            "clip_t_sub_mean",
            "clip_t_global_mean",
            "clip_i_anchor_mean",
            "dino_anchor_mean",
            "facesim_anchor_mean",
            "fgis_anchor_mean",
            "facesim_valid_ratio",
            "fgis_valid_ratio",
        ],
    )
    write_csv(
        output_dir / "per_image.csv",
        per_image_rows,
        [
            "prompt_id",
            "expected_images",
            "found_images",
            "image_index",
            "image_name",
            "image_path",
            "is_anchor",
            "sub_prompt_used",
            "global_prompt",
            "clip_t_sub",
            "clip_t_global",
            "clip_i_anchor",
            "dino_anchor",
            "facesim_anchor",
            "fgis_anchor",
            "face_detected",
            "facial_region_ready",
        ],
    )

    print(f"Evaluated {len(prompt_specs)} prompt groups.")
    print(f"Found {len(per_image_rows)} generated images under {image_root}.")
    print(f"Saved summary to: {output_dir / 'summary.json'}")
    print(f"Saved group metrics to: {output_dir / 'per_group.csv'}")
    print(f"Saved image metrics to: {output_dir / 'per_image.csv'}")


if __name__ == "__main__":
    main()
